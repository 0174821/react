package cn.jxufe.db.domain;

public class user {
	private String userno;
	private String username;
	private String registercardno;
	private String registerphoneno;
	private String password;
	public String getUserno() {
		return userno;
	}
	public void setUserno(String userno) {
		this.userno = userno;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getRegistercardno() {
		return registercardno;
	}
	public void setRegistercardno(String registercardno) {
		this.registercardno = registercardno;
	}
	public String getRegisterphoneno() {
		return registerphoneno;
	}
	public void setRegisterphoneno(String registerphoneno) {
		this.registerphoneno = registerphoneno;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
}
